package com.trgis.ticg.core.exception;

public class ShareRoleException extends Exception {
	private static final long serialVersionUID = 1L;

	public ShareRoleException() {
		super();
		
	}

	public ShareRoleException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public ShareRoleException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public ShareRoleException(String message) {
		super(message);
		
	}

	public ShareRoleException(Throwable cause) {
		super(cause);
		
	}
}
